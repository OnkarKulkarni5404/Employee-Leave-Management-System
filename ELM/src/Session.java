



public class Session {

	static String id;
	static String name;
	
	Session()
	{
		this.id="";
		this.name="";
	}
	
	public static String getId() {
		return id;
	}

	public static void setId(String id) {
		Session.id = id;
	}

	public static String getName() {
		return name;
	}

	public static void setName(String name) {
		Session.name = name;
	}

	
	
	
	
	
}
